/**
 * Get a random task from the entire collection
 */
export function getRandomTask() {
  const randomIndex = Math.floor(Math.random() * syllabusTasks.length);
  return syllabusTasks[randomIndex];
}

/**
 * Get tasks filtered by class
 * @param {number} classNumber - 9 or 10
 */
export function getTasksByClass(classNumber) {
  return syllabusTasks.filter(task => task.class === classNumber);
}

/**
 * Get tasks filtered by book
 * @param {string} bookName - "Beehive", "Moments", "First Flight", "Footprints Without Feet", "Syllabus Topics"
 */
export function getTasksByBook(bookName) {
  return syllabusTasks.filter(task => task.book === bookName);
}

/**
 * Get a random task for a specific class
 * @param {number} classNumber - 9 or 10
 */
export function getRandomTaskByClass(classNumber) {
  const classTasks = getTasksByClass(classNumber);
  const randomIndex = Math.floor(Math.random() * classTasks.length);
  return classTasks[randomIndex];
}

/**
 * Weekly cycle helper - returns task based on day of week
 * Mon-Wed: Class 9, Thu-Sat: Class 10, Sun: Value questions
 */
export function getTaskForToday() {
  const day = new Date().getDay(); // 0 = Sunday, 1 = Monday, etc.
  
  if (day === 0) {
    // Sunday: Value questions (stories with moral lessons)
    const valueTaskIds = [6, 13, 14, 15, 17, 19, 25, 28, 33, 35]; // Chapters with strong moral lessons
    const randomId = valueTaskIds[Math.floor(Math.random() * valueTaskIds.length)];
    return syllabusTasks.find(task => task.id === randomId);
  } else if (day >= 1 && day <= 3) {
    // Mon-Wed: Class 9
    return getRandomTaskByClass(9);
  } else {
    // Thu-Sat: Class 10
    return getRandomTaskByClass(10);
  }
}

